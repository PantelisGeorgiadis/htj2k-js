/**
 * Builds a VLC decoding table from the source table.
 * The source table contains entries of the form:
 * [scQ, srho, suOff, seK, se1, scwd, scwdLen]
 * where:
 * - scQ: quantized coefficient level (0-15)
 * - srho: sign and run-length of the coefficient
 * - suOff: offset for the suffix bits
 * - seK: number of suffix bits for the coefficient level
 * - se1: number of suffix bits for the run-length
 * - scwd: VLC codeword for the coefficient level and run-length
 * - scwdLen: length of the VLC codeword in bits
 *
 * The VLC decoding table is indexed by the 10-bit prefix of the VLC codeword (up to 10 bits).
 * Each entry in the VLC decoding table contains:
 * - srho: sign and run-length of the coefficient
 * - suOff: offset for the suffix bits
 * - seK: number of suffix bits for the coefficient level
 * - se1: number of suffix bits for the run-length
 * - scwdLen: length of the VLC codeword in bits
 * The function iterates through all possible 10-bit prefixes and fills the VLC decoding table
 * based on the source table entries. If a prefix matches the VLC codeword of an entry in the source table,
 * the corresponding decoding information is stored in the VLC decoding table at the index of that prefix.
 * This allows for efficient decoding of VLC codes during the decoding process, as the decoder can quickly
 * look up the decoding information based on the prefix of the VLC codeword.
 * @param {Uint16Array} tbl - The VLC decoding table to be filled.
 * @param {Array} src - The source table containing VLC codeword information.
 */
function _buildVlcTable(tbl, src) {
  for (let i = 0; i < 1024; i++) {
    const cwd = i & 0x7f;
    const cQ = i >> 7;
    for (let j = 0; j < src.length; j++) {
      const [scQ, srho, suOff, seK, se1, scwd, scwdLen] = src[j];
      if (scQ === cQ && scwd === (cwd & ((1 << scwdLen) - 1))) {
        tbl[i] = (srho << 4) | (suOff << 3) | (seK << 12) | (se1 << 8) | scwdLen;
      }
    }
  }
}
